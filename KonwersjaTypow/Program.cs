using System;

namespace KonwersjaTypow
{
    class JawnaKonwersja
    {
        static void Main(string[] args)
        {

            int i = 0;
            Console.WriteLine("i++ = " + (i++));
            Console.WriteLine("++i = " + (++i));
            Console.WriteLine("i-- = " + (i--));
            Console.WriteLine("--i = " + (--i));


        }
    }
